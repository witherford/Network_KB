let updated = 0, alreadyHadExample = 0, addedNew = 0, missingSection = 0;
const dupChecker = new Set();

for (const e of ENTRIES) {
  if (dupChecker.has(e.cmd)) {
    console.warn(`  (duplicate in ENTRIES, skipped): ${e.cmd}`);
    continue;
  }
  dupChecker.add(e.cmd);

  let found = null, foundSection = null;
  for (const [sec, cmds] of Object.entries(NS.sections)) {
    for (const c of cmds) {
      if (c.cmd === e.cmd) { found = c; foundSection = sec; break; }
    }
    if (found) break;
  }

  if (found) {
    if (!found.example) {
      found.example = e.example;
      updated++;
    } else {
      alreadyHadExample++;
    }
    if (!found.desc && e.desc) found.desc = e.desc;
  } else {
    if (!e.section) {
      console.warn(`  (no section, cannot add): ${e.cmd}`);
      missingSection++;
      continue;
    }
    NS.sections[e.section] ||= [];
    NS.sections[e.section].push({
      cmd: e.cmd,
      desc: e.desc || '',
      type: e.type || 'show',
      flagged: false,
      example: e.example
    });
    addedNew++;
  }
}

